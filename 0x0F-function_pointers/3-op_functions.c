#include "3-calc.h"

/**
 * op_add - performs addition
 * @a: first number
 * @b: second number
 * Return: sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - performs subtraction
 * @a: first number
 * @b: second number
 * Return: subtraction result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - performs addition
 * @a: first number
 * @b: second number
 * Return: product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - performs addition
 * @a: first number
 * @b: second number
 * Return: quotient of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - performs addition
 * @a: first number
 * @b: second number
 * Return: remainder of adivided by  b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
